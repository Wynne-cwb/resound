import Foundation

/// 面向用户的中文生成（摘要 / 问答 / 汇总）共用的提示词片段。集中一处，避免三处漂移。

/// 书写规范：盘古之白——中文与英文字母 / 数字之间加一个空格，提升中英混排可读性。
public let zhWritingStyle = """
书写规范：中文与英文字母之间、中文与数字之间各加一个半角空格\
（例：「我们2026年的目标是把Notification推上线」→「我们 2026 年的目标是把 Notification 推上线」）。\
但英文与数字直接相连的词不要拆开加空格（如 Q2、v3、H5、GPT-4）；\
与中文标点（，。、；：）相邻处也不加空格。
"""

/// 问答用的「今天」锚点：让综合/汇总回答涉及相对时间时不臆测年份。
public func todayAnchor(_ now: Date = Date()) -> String {
    "今天是 \(localDate(now))（\(weekdayZh(now))）。回答涉及「最近 / 今年 / 上周 / 这个季度」等相对时间时以此为基准，不要自行假设或编造年份。"
}
